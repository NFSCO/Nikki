		/// Unlocks a performance or visual upgrade.
		/// </summary>
		Upgrade = 1,
		
		/// <summary>
		/// Unlocks a car.
		/// </summary>
		Car = 2,

		/// <summary>
		/// Unlocks a track.
		/// </summary>
		Track = 3,
	}
}
