namespace Nikki.Reflection.Enum
{
	/// <summary>
	/// Enum with values False (representing boolean false) and True (representing boolean true).
	/// </summary>
	public enum eBoolean : byte
	{
		/// <summary>
		/// Represents <see langword="false"/>
		/// </summary>
		False = 0,

		/// <summary>
		/// Represents <see langword="true"/>
		/// </summary>
		True = 1,
	}
}