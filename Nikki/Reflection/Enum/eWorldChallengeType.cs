namespace Nikki.Reflection.Enum
{
	/// <summary>
	/// Enum of world challenge types in Underground 2.
	/// </summary>
	public enum eWorldChallengeType : byte
	{
		/// <summary>
		/// Unlocks visual upgrade.
		/// </summary>
		Visual = 1,

		/// <summary>
		/// Unlocks performance upgrade.
		/// </summary>
		Performance = 2,

		/// <summary>
		/// Is a showcase event.
		/// </summary>
		Showcase = 4,
	}
}
